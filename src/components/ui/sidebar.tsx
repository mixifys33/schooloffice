interface SidebarContextProps {
  state: 'expanded' | 'collapsed';
}

const SidebarContext = React.createContext<SidebarContextProps>({
  state: 'expanded',
});

interface SidebarProviderProps {
  children: React.ReactNode;
  defaultOpen?: boolean;
  open?: boolean;
  onOpenChange?: (open: boolean) => void;
}

const SidebarProvider = ({
  children,
  defaultOpen = true,
  open: openProp,
  onOpenChange: setOpenProp,
}: SidebarProviderProps) => {
  const [openState, setOpenState] = React.useState(defaultOpen);
  const open = openProp ?? openState;

  const toggleSidebar = React.useCallback(() => {
    setOpenProp ? setOpenProp(!open) : setOpenState(!openState);
  }, [setOpenProp, open, openState]);

  const contextValue = React.useMemo(
    () => ({
      state: open ? 'expanded' : 'collapsed',
      open,
      setOpen: setOpenProp ?? setOpenState,
      isMobile: false, // Simplified for this implementation
      isCollapsed: !open,
      toggleSidebar,
    }),
    [open, setOpenProp, toggleSidebar]
  );

  return (
    <SidebarContext.Provider value={contextValue}>
      <div className="flex h-full w-full group peer has-[[data-sidebar=mobile]]:rotate-0">
        {children}
      </div>
    </SidebarContext.Provider>
  );
};

interface SidebarProps
  extends React.ComponentProps<'div'>,
    VariantProps<typeof sidebarVariants> {
  side?: 'left' | 'right';
  variant?: 'default' | 'inset';
  collapsible?: 'offcanvas' | 'icon' | 'none';
}

const Sidebar = React.forwardRef<
  HTMLDivElement,
  SidebarProps
>(
  (
    {
      className,
      variant = 'default',
      collapsible = 'offcanvas',
      children,
      ...props
    },
    ref
  ) => {
    const { isMobile, state } = React.useContext(SidebarContext);

    return (
      <div
        ref={ref}
        className={cn(
          'flex h-full border-r bg-sidebar text-sidebar-foreground',
          'group-data-[variant=inset]:bg-sidebar group-data-[variant=inset]:border-r',
          className
        )}
        {...props}
      >
        <SidebarContext.Provider value={{ state: state }}>
          {children}
        </SidebarContext.Provider>
      </div>
    );
  }
);
Sidebar.displayName = 'Sidebar';

